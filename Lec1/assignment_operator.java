package Lec1;

public class assignment_operator {
    public static void main(String[] args) {
        int x=5;
        x+=3;
        System.out.println(x);
        int p=5;
        p-=5;
        System.out.println(p);
        int a=9;
        a*=5;
        System.out.println(a);
    }
}
